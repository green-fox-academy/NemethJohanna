package OP02_BlogPost;

public class BlogPost {

    /*Create a BlogPost class that has
     * an authorName
     * a title
     * a text
     * a publicationDate*/

    String authorName;
    String title;
    String text;
    String date;

    public BlogPost (String authorName, String title, String text, String date) {
        this.authorName = authorName;
        this.title = title;
        this.text = text;
        this.date = date;
    }




}
